function rhos = eval_rho_radp(s, preset)
% *************************************************************************
% *************************************************************************
% *************************************************************************
%
% EVALUATE MISCELLANEOUS FUNCTIONS
%
% Brent Wallace  
%
% 2021-11-30
%
% This program, given s >=0 and preset tag evaluates the function \rho(s).
% See under eqn. (31) of
%
%   Y. Jiang, Z.-P. Jiang. "Robust Adaptive Dynamic Programming and
%   Feedback Stabilization of Nonlinear Systems." IEEE Transactions on
%   Neural Networks and Learning Systems, 25:882-893, 2014.
%
% for a description of \rho.
%
% *************************************************************************
%
% CALL SYNTAX
%
% *************************************************************************
%
% rhos = eval_rho_radp(s, preset)
%
% *************************************************************************
%
% INPUTS
%
% *************************************************************************
%
% s         Input argument (>= 0)
%
% preset    String containing the specific preset to evaluate \rho(s) for
%
% *************************************************************************
%
% OUTPUTS
%
% *************************************************************************
%
% rhos      \rho(s) (double)
%
% *************************************************************************
% *************************************************************************
% *************************************************************************


% *************************************************************************
% *************************************************************************
% *************************************************************************
%
% BEGIN MAIN
% 
% *************************************************************************
% *************************************************************************
% *************************************************************************

switch preset


    % ***********************
    %
    % Y. JIANG, Z.-P. JIANG (2014) -- JET ENGINE EXAMPLE 
    %
    %     
    
    case 'jiang_jiang_2014_engine_ex'
        
%         rhos = 0.01 * s;            % As described in the paper
        rhos = log(0.01 * s + 2);   % As implemented in Y. Jiang's code
        
                
                
    % ***********************
    %
    % THROW ERROR IF TAG DOES NOT COME UP A MATCH
    %   
    
    otherwise
        
        error('*** ERROR: PRESET TAG NOT RECOGNIZED ***');

end