function fx = eval_misc(x, tag)
% *************************************************************************
% *************************************************************************
% *************************************************************************
%
% EVALUATE MISCELLANEOUS FUNCTIONS
%
% Brent Wallace  
%
% 2021-11-06
%
% This program, given a state vector x in R^n and tag evaluates the
% miscellaneous function f(x) associated with the tag.
%
% *************************************************************************
%
% CALL SYNTAX
%
% *************************************************************************
%
% fx = eval_misc(x, tag)
%
% *************************************************************************
%
% INPUTS
%
% *************************************************************************
%
% x         (n-dim. vector) Input.
% tag       (String) Tag of the function to evaluate
%
% *************************************************************************
%
% OUTPUTS
%
% *************************************************************************
%
% fx    	(m-dim. vector) Output of function f(x)
%
% *************************************************************************
% *************************************************************************
% *************************************************************************


% *************************************************************************
% *************************************************************************
% *************************************************************************
%
% BEGIN MAIN
% 
% *************************************************************************
% *************************************************************************
% *************************************************************************

switch tag

    % ***********************
    %
    % VRABIE, LEWIS (2009) -- OPTIMAL VALUE V^*
    %
    
    case 'vrabie_lewis_2009_hard_Vstar'
        
        fx = 0.5*x(1)^2 + x(2)^2 + x(2)^4;

    % ***********************
    %
    % VRABIE, LEWIS (2009) -- OPTIMAL VALUE V^*
    %
    
    case 'vrabie_lewis_2009_hard_mustar'
        
        fx = - sin(x(1)) * (x(2) + 2 * x(2)^3);
    
    % *********************************************************************
    % *********************************************************************
    %
    % THROW ERROR IF TAG DOES NOT COME UP A MATCH
    %  
    % *********************************************************************
    % *********************************************************************
    
    otherwise
        
        error('*** ERROR: FUNCTION TAG NOT RECOGNIZED ***');

end